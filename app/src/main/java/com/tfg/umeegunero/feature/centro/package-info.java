/**
 * Módulo de gestión de centros educativos de UmeEgunero.
 * 
 * Este módulo implementa todas las funcionalidades relacionadas con la gestión
 * de centros educativos desde la perspectiva del propio centro, incluyendo la
 * administración de alumnos, profesores, clases y comunicaciones.
 * 
 * ## Estructura del módulo
 * 
 * ### Pantallas ({@code screen/})
 * - {@code CentroDashboardScreen}: Panel principal del centro
 * - {@code GestionAlumnosScreen}: Gestión de alumnos matriculados
 * - {@code GestionClasesScreen}: Administración de clases y cursos
 * - {@code ComunicacionesScreen}: Sistema de comunicación con familias
 * 
 * ### ViewModels ({@code viewmodel/})
 * - {@code CentroDashboardViewModel}: Lógica del panel principal
 * - {@code GestionAlumnosViewModel}: Gestión de alumnos
 * - {@code GestionClasesViewModel}: Gestión de clases
 * - {@code ComunicacionesViewModel}: Gestión de comunicaciones
 * 
 * ## Características principales
 * - Gestión completa del centro educativo
 * - Administración de personal docente
 * - Gestión de matrículas y alumnado
 * - Organización de clases y cursos
 * - Sistema de comunicación integrado
 * 
 * ## Funcionalidades clave
 * - Alta y baja de alumnos
 * - Asignación de profesores a clases
 * - Gestión de horarios
 * - Comunicación con familias
 * - Seguimiento académico
 * 
 * @see com.tfg.umeegunero.feature.centro.screen
 * @see com.tfg.umeegunero.feature.centro.viewmodel
 * @version 1.0
 */
